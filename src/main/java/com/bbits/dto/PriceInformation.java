package com.bbits.dto;

import java.util.Date;

public class PriceInformation {
	
	private Date date;
	
	private float price;

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

}
